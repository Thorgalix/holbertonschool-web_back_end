#!/usr/bin/env python3
"""Module for adding two floats with type annotations.

This module provides a simple function to add two floating-point numbers
and demonstrates the use of type annotations in Python.
"""


def add(a: float, b: float) -> float:
    """Add two floats and return their sum.

    Args:
        a: The first float number to add.
        b: The second float number to add.

    Returns:
        The sum of the two input floats.
    """
    return a + b
