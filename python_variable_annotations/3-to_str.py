#!/usr/bin/env python3
"""Module for converting a float to a string with type annotations.

This module provides a simple function to convert a floating-point number
to its string representation and demonstrates type annotations in Python.
"""


def to_str(n: float) -> str:
    """Convert a float to a string.

    Args:
        n: The floating-point number to convert.

    Returns:
        The string representation of n.
    """
    return str(n)
